function H_3x3 = computeHomography(src_pts_nx2, dest_pts_nx2)

xs = src_pts_nx2(:,1);
ys = src_pts_nx2(:,2);

xd = dest_pts_nx2(:,1);
yd = dest_pts_nx2(:,2);

A = [xs(1) ys(1) 1 0 0 0 -xd(1)*xs(1) -xd(1)*ys(1) -xd(1);...
    0 0 0 xs(1) ys(1) 1 -yd(1)*xs(1) -yd(1)*ys(1) -yd(1);...
    xs(2) ys(2) 1 0 0 0 -xd(2)*xs(2) -xd(2)*ys(2) -xd(2);...
    0 0 0 xs(2) ys(2) 1 -yd(2)*xs(2) -yd(2)*ys(2) -yd(2);...
    xs(3) ys(3) 1 0 0 0 -xd(3)*xs(3) -xd(3)*ys(3) -xd(3);...
    0 0 0 xs(3) ys(3) 1 -yd(3)*xs(3) -yd(3)*ys(3) -yd(3);...
    xs(4) ys(4) 1 0 0 0 -xd(4)*xs(4) -xd(4)*ys(4) -xd(4);...
    0 0 0 xs(4) ys(4) 1 -yd(4)*xs(4) -yd(4)*ys(4) -yd(4)];


[H,D]= eig(A'*A);
%[l m] = size(H)

H_3x3 = [H(1,1) H(2,1) H(3,1); H(4,1) H(5,1) H(6,1); H(7,1) H(8,1) H(9,1)];